package temp1;

public class Animal {
    private String name;
    private int leg;
    private int speed;
    private double weight;
    private int age;

    public void setName(String name) {
        this.name = name;
    }

    public void setLeg(int leg) {
        this.leg = leg;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
